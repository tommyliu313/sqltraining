SELECT TIMESTAMP '2013-01-01 00:00:00' AS BEGYR_TS,
       PATIENT_ID, SYSTEM_TIMESTAMP
       FROM RX_FILL_INFO
       WHERE SYSTEM_TIMESTAMP BETWEEN TIMESTAMP '2013-11-19 10:41:01.000'
                                AND TIMESTAMP '2013-11-19 10:41:01.360'
             AND SYSTEM_TIMESTAMP IN (TIMESTAMP '2013-11-19 10:41:01.337',
                                      TIMESTAMP '2013-11-19 10:41:01.352')
        ORDER BY SYSTEM_TIMESTAMP;                                      