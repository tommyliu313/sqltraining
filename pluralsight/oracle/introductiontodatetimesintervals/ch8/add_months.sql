SELECT DATE '2012-03-29' AS MAR2012,
       ADD_MONTHS(DATE '2012-03-29', +1) AS APR2012,
       ADD_MONTHS(DATE '2012-03-29', -1) AS FEB2012,
       DATE '2011-03-29' AS MAR2011,
       ADD_MONTHS(DATE '2011-03-29', +1) AS APR2011,
       ADD_MONTHS(DATE '2011-03-29', -1) AS FEB2011,
FROM DUAL;       